class VGG(nn.Module):
    '''
    VGG model
    '''

    def __init__(self, features, has_gate=False, struct=False):
        super(VGG, self).__init__()
        self.has_gate = has_gate
        self.struct = struct
        self.features = features
        self.classifier = nn.Sequential(
            nn.Dropout(),
            nn.Linear(512, 512),
            nn.ReLU(True),
            nn.Dropout(),
            nn.Linear(512, 512),
            nn.ReLU(True),
            nn.Linear(512, 10),
        )
        if has_gate:
            self.pclassifier = nn.Sequential(
                nn.Dropout(),
                nn.Linear(512, 512),
                nn.ReLU(True),
                nn.Dropout(),
                nn.Linear(512, 512),
                nn.ReLU(True),
                nn.Linear(512, 10),
            )
            if self.struct:
                self.gate = nn.Linear(3 * 32 * 32, 1)
            else:
                self.gate = nn.Linear(512, 1)
        # Initialize weights
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                n = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
                m.weight.data.normal_(0, math.sqrt(2. / n))
                m.bias.data.zero_()

    def forward(self, input):
        x = self.features(input)
        x = x.view(x.size(0), -1)
        if self.has_gate:
            if self.struct:
                g = torch.sigmoid(self.gate(torch.flatten(input, 1)))
            else:
                g = torch.sigmoid(self.gate(x))
            y = self.classifier(x)
            z = self.pclassifier(x)
            return y * g + z * (1-g), g, z
        else:
            x = self.classifier(x)
            return x


def make_layers(cfg, batch_norm=False):
    layers = []
    in_channels = 3
    for v in cfg:
        if v == 'M':
            layers += [nn.MaxPool2d(kernel_size=2, stride=2)]
        else:
            conv2d = nn.Conv2d(in_channels, v, kernel_size=3, padding=1)
            if batch_norm:
                layers += [conv2d, nn.BatchNorm2d(v), nn.ReLU(inplace=True)]
            else:
                layers += [conv2d, nn.ReLU(inplace=True)]
            in_channels = v
    return nn.Sequential(*layers)


